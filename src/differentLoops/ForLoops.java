package differentLoops;
public class ForLoops {

    public static void main(String[] args) {

        for (int i = 10; i >= 0; i--){
            System.out.println(i);
        }
        System.out.println("Happy new Year!");
    }
}
// for loop will execute a block of code a limited amount of times.
// in () total of 3 statements= index to declare, condition, increment or decrement the index by a certain amount